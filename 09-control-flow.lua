-- if statement
if 1 < 2 then
    print("1 is less than 2")
end

-- if else statement
if 1 > 2 then
    print("1 is greater than 2")
else
    print("1 is not greater than 2")
end

-- if elseif else statement
if 1 > 2 then
    print("1 is greater than 2")
elseif 1 < 2 then
    print("1 is less than 2")
else
    print("1 is not greater than 2 and 1 is not less than 2")
end